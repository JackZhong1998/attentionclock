import SwiftUI

enum MCPixel: UInt8 {
    case empty = 0
    case outline = 1
    case fur = 2
    case dark = 3
    case light = 4
    case eye = 5
    case blink = 6
    case nose = 7
}

enum MCPalette {
    static func color(for pixel: MCPixel) -> Color {
        switch pixel {
        case .empty: return .clear
        case .outline: return Color(red: 0.25, green: 0.18, blue: 0.12)
        case .fur: return Color(red: 0.83, green: 0.63, blue: 0.29)
        case .dark: return Color(red: 0.55, green: 0.35, blue: 0.16)
        case .light: return Color(red: 0.94, green: 0.75, blue: 0.38)
        case .eye: return Color(red: 0.12, green: 0.11, blue: 0.10)
        case .blink: return Color(red: 0.55, green: 0.35, blue: 0.16)
        case .nose: return Color(red: 0.78, green: 0.45, blue: 0.42)
        }
    }
}

typealias MCPixelGrid = [[MCPixel]]

enum MCCatSprites {
    static let gridSize = 16

    static func grid(for frame: CatFrame) -> MCPixelGrid {
        switch frame {
        case .frontBase: return frontBase
        case .frontEar1: return frontEar1
        case .frontEar2: return frontEar2
        case .frontBlink: return frontBlink
        case .walkLeft1: return walkLeft1
        case .walkLeft2: return walkLeft2
        case .walkRight1: return mirror(walkLeft1)
        case .walkRight2: return mirror(walkLeft2)
        }
    }

