    private static func mirror(_ grid: MCPixelGrid) -> MCPixelGrid {
        grid.map { row in
            let padded = row + Array(repeating: MCPixel.empty, count: max(0, gridSize - row.count))
            return Array(padded.prefix(gridSize).reversed())
        }
    }

    private static func parse(_ art: String) -> MCPixelGrid {
        art.split(separator: "\n").map { line in
            var row = Array(line.prefix(gridSize)).map { char -> MCPixel in
                switch char {
                case ".": return .empty
                case "k": return .outline
                case "f": return .fur
                case "d": return .dark
                case "l": return .light
                case "e": return .eye
                case "b": return .blink
                case "n": return .nose
                default: return .empty
                }
            }
            while row.count < gridSize { row.append(.empty) }
            return row
        }
    }
}
